WITH RECURSIVE GENERATION_LEVEL AS (
    SELECT ID, PARENT_ID, 1 AS GEN
    FROM ECOLI_DATA
    WHERE PARENT_ID IS NULL
    
    UNION ALL
    
    SELECT ed.ID, ed.PARENT_ID, gl.GEN + 1
    FROM ECOLI_DATA ed
    JOIN GENERATION_LEVEL gl 
    ON ed.PARENT_ID = gl.ID
)

SELECT COUNT(gl.GEN) AS COUNT, gl.GEN AS GENERATION
FROM GENERATION_LEVEL AS gl
WHERE ID NOT IN (
    SELECT PARENT_ID
    FROM ECOLI_DATA
    WHERE PARENT_ID IS NOT NULL
)
GROUP BY gl.GEN
ORDER BY gl.GEN