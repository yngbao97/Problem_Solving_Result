SELECT a.SCORE AS SCORE,
        a.EMP_NO AS EMP_NO,
        b.EMP_NAME AS EMP_NAME,
        b.POSITION AS POSITION,
        b.EMAIL AS EMAIL
FROM (SELECT SUM(SCORE) AS SCORE, EMP_NO
        FROM HR_GRADE
        WHERE YEAR = 2022
        GROUP BY EMP_NO
        ORDER BY SCORE DESC LIMIT 1) AS a
    JOIN HR_EMPLOYEES AS b
    ON a.EMP_NO = b.EMP_NO