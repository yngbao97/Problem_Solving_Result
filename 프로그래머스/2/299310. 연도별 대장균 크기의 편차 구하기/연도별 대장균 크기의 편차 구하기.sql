SELECT YEAR(DIFFERENTIATION_DATE) AS YEAR, m.MAX - SIZE_OF_COLONY AS YEAR_DEV, a.ID
FROM ECOLI_DATA AS a, (SELECT MAX(SIZE_OF_COLONY) AS MAX, YEAR(DIFFERENTIATION_DATE) AS YEAR
                        FROM ECOLI_DATA
                        GROUP BY YEAR(DIFFERENTIATION_DATE)) AS m
WHERE YEAR(DIFFERENTIATION_DATE) = m.YEAR
ORDER BY YEAR, YEAR_DEV

