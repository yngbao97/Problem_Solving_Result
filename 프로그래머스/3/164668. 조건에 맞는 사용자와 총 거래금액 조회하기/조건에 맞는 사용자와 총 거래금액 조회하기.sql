SELECT a.WRITER_ID AS USER_ID, b.NICKNAME, a.SUM AS TOTAL_SALES
FROM (SELECT WRITER_ID, SUM(PRICE) AS SUM
        FROM USED_GOODS_BOARD
        WHERE STATUS = 'DONE'
        GROUP BY WRITER_ID
        HAVING SUM(PRICE) >= 700000) AS a, USED_GOODS_USER AS b
WHERE a.WRITER_ID = b.USER_ID
ORDER BY TOTAL_SALES