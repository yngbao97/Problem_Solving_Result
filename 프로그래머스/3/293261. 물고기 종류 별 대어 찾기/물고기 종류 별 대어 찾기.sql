SELECT a.ID AS ID, b.FISH_NAME AS FISH_NAME, a.LENGTH AS LENGTH
FROM FISH_INFO as a
    JOIN FISH_NAME_INFO as b ON a.FISH_TYPE = b.FISH_TYPE
WHERE (a.FISH_TYPE, a.LENGTH) IN (SELECT FISH_TYPE as TYPE, MAX(LENGTH) AS LENGTH
                                FROM FISH_INFO
                                GROUP BY FISH_TYPE)
ORDER BY a.ID